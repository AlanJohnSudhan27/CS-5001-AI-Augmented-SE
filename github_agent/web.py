def _create_llm() -> OllamaLLM:
    return OllamaLLM(model=OLLAMA_MODEL, host=OLLAMA_HOST)


def _parse_github_repo(repo_input: str) -> tuple[str, str]:
    """Return (owner, repo_name) from a GitHub URL, owner/repo string, or local path."""
    s = (repo_input or "").strip().rstrip("/")
    if s.endswith(".git"):
        s = s[:-4]

    if "github.com" in s:
        after = s.split("github.com/")[-1]
        parts = [p for p in after.split("/") if p]
        if len(parts) >= 2:
            return parts[0], parts[1]
        return "", ""

    # owner/repo shorthand (not a file path)
    if "/" in s and not s.startswith("/") and not s.startswith(".") and s.count("/") == 1:
        parts = s.split("/")
        return parts[0], parts[1]

    # local path — try git remote
    local = s or "."
    try:
        r = subprocess.run(
            ["git", "remote", "get-url", "origin"],
            cwd=local, capture_output=True, text=True, timeout=5,
        )
        if r.returncode == 0 and r.stdout.strip():
            return _parse_github_repo(r.stdout.strip())
    except Exception:
        pass
    return "", ""


def _local_path(repo_input: str) -> str:
    """Return the local filesystem path for git operations."""
    s = (repo_input or "").strip()
    if (
        "github.com" in s
        or (s and "/" in s and not s.startswith("/") and not s.startswith(".") and s.count("/") == 1)
    ):
        return "."
    return s or "."


def _package_to_dict(package) -> dict:
    draft = package.draft
    reflection = package.reflection

    if package.draft_type == "issue":
        draft_dict = {
            "title": draft.title,
            "problem_description": draft.problem_description,
            "evidence": draft.evidence,
            "acceptance_criteria": draft.acceptance_criteria,
            "risk_level": draft.risk_level,
            "labels": draft.labels,
        }
    else:
        draft_dict = {
            "title": draft.title,
            "summary": draft.summary,
            "files_affected": draft.files_affected,
            "behavior_change": draft.behavior_change,
            "test_plan": draft.test_plan,
            "risk_level": draft.risk_level,
            "base_branch": draft.base_branch,
        }

    return {
        "draft_type": package.draft_type,
        "draft": draft_dict,
        "reflection": {
            "passed": reflection.passed,
            "unsupported_claims": reflection.unsupported_claims,
            "missing_evidence": reflection.missing_evidence,
            "missing_tests": reflection.missing_tests,
            "policy_violations": reflection.policy_violations,
            "suggestions": reflection.suggestions,
            "overall_quality": reflection.overall_quality,
        },
        "reviewer_summary": package.reviewer_summary,
    }


def _format_issue_body(draft: dict) -> str:
    criteria = "\n".join(f"- [ ] {c}" for c in draft.get("acceptance_criteria", []))
    return (
        f"## Problem Description\n\n{draft.get('problem_description', '')}\n\n"
        f"## Evidence\n\n{draft.get('evidence', '')}\n\n"
        f"## Acceptance Criteria\n\n{criteria or '- [ ] TBD'}\n\n"
        f"**Risk Level:** {draft.get('risk_level', 'medium').upper()}\n\n"
        "_Generated by GitHub Review Agent_"
    )


def _format_pr_body(draft: dict) -> str:
    files = "\n".join(f"- `{f}`" for f in draft.get("files_affected", []))
    return (
        f"## Summary\n\n{draft.get('summary', '')}\n\n"
        f"## Files Affected\n\n{files or '_See diff_'}\n\n"
        f"## Behavior Change\n\n{draft.get('behavior_change', '')}\n\n"
        f"## Test Plan\n\n{draft.get('test_plan', '')}\n\n"
        f"**Risk Level:** {draft.get('risk_level', 'medium').upper()}\n\n"
        "_Generated by GitHub Review Agent_"
    )


def _format_improvement_comment(improved: dict, content_type: str, critique: dict) -> str:
    issues_found = []
    for key in ("vague_language", "missing_information", "unclear_criteria", "structural_problems"):
        items = critique.get(key, [])
        if items:
            issues_found.append(f"**{key.replace('_', ' ').title()}:** " + "; ".join(str(x) for x in items))
    critique_summary = "\n".join(f"- {line}" for line in issues_found) or "- No major issues found."

    if content_type == "issue":
        criteria = "\n".join(f"- [ ] {c}" for c in improved.get("acceptance_criteria", []))
        body = (
            f"**Suggested Title:** {improved.get('title', '')}\n\n"
            f"**Improved Description:**\n{improved.get('problem_description', '')}\n\n"
            f"**Evidence:**\n{improved.get('evidence', '')}\n\n"
            f"**Acceptance Criteria:**\n{criteria or '- [ ] TBD'}\n\n"
            f"**Risk Level:** {improved.get('risk_level', 'medium').upper()}"
        )
    else:
        files = "\n".join(f"- `{f}`" for f in improved.get("files_affected", []))
        body = (
            f"**Suggested Title:** {improved.get('title', '')}\n\n"
            f"**Improved Summary:**\n{improved.get('summary', '')}\n\n"
            f"**Files Affected:**\n{files or '_Not specified_'}\n\n"
            f"**Behavior Change:**\n{improved.get('behavior_change', '')}\n\n"
            f"**Test Plan:**\n{improved.get('test_plan', '')}\n\n"
            f"**Risk Level:** {improved.get('risk_level', 'medium').upper()}"
        )

    return (
        "## Improvement Suggestion\n\n"
        "_Generated by GitHub Review Agent._\n\n"
        "### Issues in Current Version\n\n"
        f"{critique_summary}\n\n"
        "---\n\n"
        "### Proposed Improved Version\n\n"
        f"{body}\n"
    )



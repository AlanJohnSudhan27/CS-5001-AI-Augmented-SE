@APP.route("/improve")
def improve_form():
    return render_template("improve.html")


@APP.route("/improve/run", methods=["POST"])
def improve_run():
    repo_input = request.form.get("github_repo_input", "").strip()
    token = request.form.get("github_token", "").strip()
    content_type = request.form.get("content_type", "issue")
    item_num_str = request.form.get("item_num", "").strip()

    if not item_num_str.isdigit():
        flash("Issue/PR number must be a positive integer.", "danger")
        return redirect(url_for("improve_form"))

    item_num = int(item_num_str)
    github_owner, github_repo_name = _parse_github_repo(repo_input)

    if not github_owner or not github_repo_name:
        flash("Could not parse a GitHub repository from the input. Use owner/repo or a GitHub URL.", "danger")
        return redirect(url_for("improve_form"))
    if not token:
        flash("GitHub token is required.", "danger")
        return redirect(url_for("improve_form"))

    try:
        api = GitHubAPI(token=token)
        existing = api.get_issue(github_owner, github_repo_name, item_num) if content_type == "issue" else api.get_pr(github_owner, github_repo_name, item_num)
    except GitHubAPIError as exc:
        flash(f"GitHub API error: {exc}", "danger")
        return redirect(url_for("improve_form"))

    try:
        orchestrator = MultiAgentOrchestrator(_create_llm())
        result = orchestrator.run_improve(existing, content_type)
    except Exception as exc:
        flash(f"Agent error: {exc}", "danger")
        return redirect(url_for("improve_form"))

    improve_id = str(uuid.uuid4())
    IMPROVE_RESULTS[improve_id] = {
        **result,
        "github_owner": github_owner,
        "github_repo": github_repo_name,
        "github_token": token,
        "item_num": item_num,
    }
    return redirect(url_for("improve_result", improve_id=improve_id))


@APP.route("/improve/result")
def improve_result():
    improve_id = request.args.get("improve_id", "")
    if not improve_id or improve_id not in IMPROVE_RESULTS:
        flash("Result not found or expired.", "warning")
        return redirect(url_for("improve_form"))
    return render_template("improve_result.html", data=IMPROVE_RESULTS[improve_id], improve_id=improve_id)


@APP.route("/improve/post", methods=["POST"])
def improve_post():
    improve_id = request.form.get("improve_id", "")
    if not improve_id or improve_id not in IMPROVE_RESULTS:
        flash("Result not found or expired.", "warning")
        return redirect(url_for("improve_form"))

    data = IMPROVE_RESULTS.pop(improve_id)
    comment_body = _format_improvement_comment(data.get("improved", {}), data.get("content_type", "issue"), data.get("critique", {}))

    try:
        api = GitHubAPI(token=data["github_token"])
        result = api.add_comment(data["github_owner"], data["github_repo"], data["item_num"], comment_body)
        flash(f"Suggestion posted: {result.get('html_url', '')}", "success")
    except Exception as exc:
        flash(f"Error posting comment: {exc}", "danger")

    return redirect(url_for("improve_form"))


def run_app(host: str = "127.0.0.1", port: int = 5000):
    APP.run(host=host, port=port, debug=False)


if __name__ == "__main__":
    run_app()

@APP.route("/draft/run", methods=["POST"])
def draft_run():
    draft_source = request.form.get("draft_source", "instruction")
    report_id = request.form.get("report_id", "").strip()
    artifact_type = request.form.get("artifact_type", "issue")
    instruction = request.form.get("instruction", "").strip()
    repo_input = request.form.get("repo", ".").strip()
    from_commit = request.form.get("from_commit", "").strip()
    to_commit = request.form.get("to_commit", "").strip()
    github_input = request.form.get("github_repo_input", "").strip()
    token = request.form.get("github_token", "").strip()
    head_branch = request.form.get("head_branch", "").strip()

    report = REPORTS.get(report_id, {}) if report_id else {}
    if draft_source == "review" and report:
        repo_input = report.get("repo_input") or repo_input
        if not from_commit:
            from_commit = (report.get("from_commit") or "").strip()
        if not to_commit:
            to_commit = (report.get("to_commit") or "").strip()

    local_path = report.get("local_path") if (draft_source == "review" and report.get("local_path")) else _local_path(repo_input)
    github_owner, github_repo_name = _parse_github_repo(github_input or repo_input)
    action = f"create_{artifact_type}"

    try:
        llm = _create_llm()
        orchestrator = MultiAgentOrchestrator(llm)
    except Exception as exc:
        flash(f"LLM error: {exc}", "danger")
        return redirect(url_for("draft_form"))

    try:
        if draft_source == "review":
            from pathlib import Path
            tools = Tools(Path(local_path).resolve())
            if not tools.is_git_repo():
                flash("Not a git repository. Check the repo path.", "danger")
                return redirect(url_for("draft_form"))

            compare_mode = (report.get("compare_mode") or "").strip() if report else ""
            base_branch = (report.get("base_branch") or "").strip() if report else ""
            if compare_mode == "commits":
                if from_commit and to_commit:
                    diff_result = tools.diff_from_commits(from_commit, to_commit)
                    commit_msgs = tools.commit_messages(from_commit, to_commit)
                elif from_commit:
                    diff_result = tools.diff_from_commits(f"{from_commit}~1", from_commit)
                    commit_msgs = []
                else:
                    flash("No commit context found for this review report.", "danger")
                    return redirect(url_for("draft_form"))
            else:
                diff_result = tools.diff_from_branch(base_branch or None)
                commit_msgs = []
            package = orchestrator.run_review_and_draft(diff_result, commit_msgs, action, instruction)
        else:
            package = orchestrator.run_explicit_draft(instruction, artifact_type)
    except Exception as exc:
        flash(f"Agent error: {exc}", "danger")
        return redirect(url_for("draft_form"))

    draft_id = str(uuid.uuid4())
    data = _package_to_dict(package)
    data["github_owner"] = github_owner
    data["github_repo"] = github_repo_name
    data["github_token"] = token
    data["head_branch"] = head_branch
    PENDING_DRAFTS[draft_id] = data

    return redirect(url_for("draft_approve", draft_id=draft_id))


@APP.route("/draft/approve")
def draft_approve():
    draft_id = request.args.get("draft_id", "")
    if not draft_id or draft_id not in PENDING_DRAFTS:
        flash("Draft not found or expired.", "warning")
        return redirect(url_for("draft_form"))
    return render_template("approve.html", data=PENDING_DRAFTS[draft_id], draft_id=draft_id)


@APP.route("/draft/confirm", methods=["POST"])
def draft_confirm():
    draft_id = request.form.get("draft_id", "")
    decision = request.form.get("decision", "reject")

    if not draft_id or draft_id not in PENDING_DRAFTS:
        flash("Draft not found or expired.", "warning")
        return redirect(url_for("draft_form"))

    data = PENDING_DRAFTS.pop(draft_id)

    if decision != "approve":
        flash("Draft rejected — nothing was created on GitHub.", "info")
        return redirect(url_for("draft_form"))

    owner = data.get("github_owner", "")
    repo_name = data.get("github_repo", "")
    token = data.get("github_token", "")
    draft_type = data.get("draft_type", "issue")
    draft = data.get("draft", {})

    if not owner or not repo_name or not token:
        flash("GitHub repository and token are required to create content.", "danger")
        return redirect(url_for("draft_form"))

    try:
        api = GitHubAPI(token=token)
        if draft_type == "issue":
            created = api.create_issue(owner, repo_name, draft.get("title", ""), _format_issue_body(draft), draft.get("labels", []))
            flash(f"Issue created: {created.get('html_url', '')}", "success")
        else:
            head_branch = data.get("head_branch", "")
            if not head_branch:
                flash("Head branch is required for PR creation.", "danger")
                return redirect(url_for("draft_form"))
            created = api.create_pr(owner, repo_name, draft.get("title", ""), _format_pr_body(draft), head_branch, draft.get("base_branch", "main"))
            flash(f"Pull request created: {created.get('html_url', '')}", "success")
    except GitHubAPIError as exc:
        flash(f"GitHub API error: {exc}", "danger")
    except Exception as exc:
        flash(f"Unexpected error: {exc}", "danger")

    return redirect(url_for("draft_form"))



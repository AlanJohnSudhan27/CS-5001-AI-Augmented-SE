@APP.route("/")
def index():
    return render_template("index.html")


@APP.route("/review", methods=["POST"])

def review():
    repo_input = request.form.get("repo", ".").strip()
    compare_mode = request.form.get("compare_mode", "branch")
    from_commit = request.form.get("from_commit", "").strip()
    to_commit = request.form.get("to_commit", "").strip()
    base_branch = request.form.get("base_branch", "").strip() or None
    token = request.form.get("token", "").strip()

    local_path = _local_path(repo_input)
    github_owner, github_repo_name = _parse_github_repo(repo_input)

    cfg = AgentConfig(repo=local_path, model=OLLAMA_MODEL, host=OLLAMA_HOST, temperature=0.0, verbose=False)
    agent = Agent(cfg)

    # Always use the LLM for model override (no skipping)

    try:
        if compare_mode == "commits":
            if from_commit and to_commit:
                result = agent.review_commits(f"{from_commit}..{to_commit}", use_colors=False, render_report=False)
            elif from_commit:
                result = agent.review_commits(f"{from_commit}~1..{from_commit}", use_colors=False, render_report=False)
            else:
                flash("Enter at least one commit hash when using Commits mode.", "warning")
                return redirect(url_for("index"))
        else:
            result = agent.review_branch(base_branch, use_colors=False, render_report=False)
    except Exception as exc:
        flash(f"Review failed: {exc}", "danger")
        return redirect(url_for("index"))

    if not result.ok:
        flash(result.details, "danger")
        return redirect(url_for("index"))

    summary = result.summary or {}
    report_id = str(uuid.uuid4())
    REPORTS[report_id] = {
        "summary": summary,
        "repo_input": repo_input,
        "local_path": local_path,
        "github_owner": github_owner,
        "github_repo": github_repo_name,
        "compare_mode": compare_mode,
        "from_commit": from_commit,
        "to_commit": to_commit,
        "base_branch": base_branch,
        "token": token,
    }

    return render_template("review.html", summary=summary, report_id=report_id, report=REPORTS[report_id])


@APP.route("/download_report")
def download_report():
    report_id = request.args.get("id")
    if not report_id or report_id not in REPORTS:
        return "Report not found", 404
    with tempfile.NamedTemporaryFile(delete=False, suffix=".json") as tmp:
        tmp.write(json.dumps(REPORTS[report_id], indent=2).encode())
        tmp_path = tmp.name
    return send_file(tmp_path, as_attachment=True, download_name="review_report.json", mimetype="application/json")


